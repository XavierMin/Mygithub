/*******************************************************************************
  发送字节
*******************************************************************************/
void uart2_send_char(u8 temp)
{
  USART_SendData(USART2, (u8)temp);
  while (USART_GetFlagStatus(USART2, USART_FLAG_TXE) == RESET);
}

/*******************************************************************************
  发送字符串
*******************************************************************************/
void uart2_send_buff(u8 buf[], u32 len)
{
  u32 i;
  for (i = 0; i < len; i++)
    uart2_send_char(buf[i]);
  // 可设置换行符   NB串口AT指令不需要换行
  uart2_send_char('\r');
  uart2_send_char('\n');
}

















